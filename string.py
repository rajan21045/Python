# indexng and slicing in string
s = "Lumbini ICT Campus"
# positive indexing
print(s[3]) 
print(s[8])
# negative indexing
print(s[-1])
print(s[-5])
# slicing
print(s[0:7])
print(s[12:])
print(s[8:11])

print("\n")
# using step methods
print("using step methods")
print(s[1:10:2])
print(s[-6:-1:2])

# reverse a string
print(s[::-1])
