first = int(input("Enter an integer: "))
second = int(input("Enter A Second Integer: "))
sum = first + second
diff = second - first
mul = first * second
div = second / first
mod = second % first
  
print(f"Sum is {sum}")
print(f"Multification is {mul}")
print(f"Difference is {diff}")
print(f"Division is {div}")
print(f"Modulus is {mod}")
